# append()
n = [1,2,3]
n.append(4)
print(n)

# extend()
n = [1,2,3,45,6]
n.append([6,7,8])
print(n)

#insert()
n = [ 1,2,34,5]
n.insert(1,23)
print(n)

#DELETE OPERATION
# Remove()
n.remove(34)

#Pop()-- Removes last Elements
n.pop()

# Clear() : Removes all elements
samplelist = [1,2,3]
samplelist.clear()

